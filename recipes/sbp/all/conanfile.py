class SbpConan(ConanFile):
    name = "sbp"
    license = "MIT"
    homepage = "https://github.com/swift-nav/libsbp"
    url = "https://github.com/conan-io/conan-center-index"
    description = "Swift Binary Protocol client library"
    topics = ("gnss",)
    settings = "os", "compiler", "build_type", "arch"
    options = {
        "shared": [True, False],
        "fPIC": [True, False],
    }
    default_options = {
        "shared": False,
        "fPIC": True,
    }
    exports_sources = "CMakeLists.txt", "c"

    def config_options(self):
        if self.settings.os == "Windows":
            del self.options.fPIC

    def configure(self):
        if self.options.shared:
            self.options.rm_safe("fPIC")

    def validate(self):
        if self.settings.os == "Windows" and self.options.shared:
            raise ConanInvalidConfiguration(
                "Windows shared builds are not supported right now, see issue https://github.com/swift-nav/libsbp/issues/1062"
            )

    def source(self):
        data = self.conan_data["sources"][self.version]

        get(self, **data["source"], strip_root=True)
        get(
            self,
            **data["cmake"],
            strip_root=True,
            destination=os.path.join(self.source_folder, "c", "cmake", "common")
        )

    def generate(self):
        tc = CMakeToolchain(self)
        tc.variables["libsbp_ENABLE_TESTS"] = False
        tc.variables["libsbp_ENABLE_DOCS"] = False
        tc.generate()

        tc = CMakeDeps(self)
        tc.generate()

    def build(self):
        cmake = CMake(self)
        cmake.configure()
        cmake.build()

    def package(self):
        copy(self, "LICENSE", src=self.source_folder, dst="licenses", ignore_case=True, keep_path=False)
        cmake = CMake(self)
        cmake.install()

    def package_info(self):
        self.cpp_info.libs = ["sbp"]
